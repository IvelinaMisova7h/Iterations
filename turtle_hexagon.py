import turtle

my_turtle = turtle.Turtle()
my_turtle.shape("turtle")

for i in range(0, 1):
    my_turtle.forward(50)
    my_turtle.left(120)
    my_turtle.backward(50)
    my_turtle.left(120)
    my_turtle.forward(50)
    my_turtle.left(120)
    my_turtle.backward(50)
    my_turtle.left(120)
    my_turtle.forward(50)
    my_turtle.left(120)
    my_turtle.backward(50)
    my_turtle.left(120)

turtle.done()
